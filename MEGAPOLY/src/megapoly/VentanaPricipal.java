    public void cargarPartida() {

        try {
            Saves = new File("Saves");
            archivo = new FileInputStream(new File(Saves,"PartidaGuardada"));
            obj = new ObjectInputStream(archivo);
            try {
                //Nombres Jugadores
                texto = (String) obj.readObject();
                texto2 = (String) obj.readObject();
                //Jugadores
                jugador1 = (Jugadores) obj.readObject();
                jugador2 = (Jugadores) obj.readObject();
                //Dinero Jugadores
                dinero1 = (int) obj.readObject();
                dinero2 = (int) obj.readObject();
                jugador1.setDinero(dinero1);
                jugador2.setDinero(dinero2);
                //Fichas Jugadores
                fichaJugador1 = (FichaTablero) obj.readObject();
                fichaJugador2 = (FichaTablero) obj.readObject();
                posicionFicha = (int) obj.readObject();
                posicionFicha2 = (int) obj.readObject();
                fichaJugador1.setPosicionFicha(posicionFicha);
                fichaJugador2.setPosicionFicha(posicionFicha2);
                //Turno
                turno = (int) obj.readObject();

            } catch (ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(null, ex.toString());
            }
            VentanaPartida2 vp = new VentanaPartida2(texto, texto2, fichaJugador1, fichaJugador2, jugador1, jugador2, turno);
            vp.setVisible(true);

        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, " Error al cargar partida " + e.toString());
        } finally {
            try {
                if (obj != null) {
                    obj.close();
                }
                if (archivo != null) {
                    archivo.close();
                }
            } catch (IOException ex) {
                System.out.println("No se ha cargado partida " + ex.toString());
            }
        }

    }

